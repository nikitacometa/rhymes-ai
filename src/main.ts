import { NestFactory } from '@nestjs/core';
import { ValidationPipe } from '@nestjs/common';
import { AppModule } from './app.module';

async function bootstrap() {
  const app = await NestFactory.create(AppModule);
  
  // –í–∫–ª—é—á–∞–µ–º –≤–∞–ª–∏–¥–∞—Ü–∏—é DTO (Data Transfer Objects)
  app.useGlobalPipes(
    new ValidationPipe({
      whitelist: true, // —É–¥–∞–ª—è–µ—Ç –ø–æ–ª—è, –∫–æ—Ç–æ—Ä—ã—Ö –Ω–µ—Ç –≤ DTO
      forbidNonWhitelisted: true, // –≤—ã–±—Ä–∞—Å—ã–≤–∞–µ—Ç –æ—à–∏–±–∫—É –ø—Ä–∏ –ª–∏—à–Ω–∏—Ö –ø–æ–ª—è—Ö
      transform: true, // –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ—Ç —Ç–∏–ø—ã
    }),
  );

  const port = process.env.PORT || 3000;
  await app.listen(port);
  
  console.log(`üöÄ RhymePadre API running on http://localhost:${port}`);
}

bootstrap();

